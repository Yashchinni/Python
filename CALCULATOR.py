#  ..........BASIC CALCULATOR.......
first_num = 10
second_num = 20
third_num = 30

print(first_num + second_num + third_num)  # Addition..
print(first_num - second_num - third_num)  # Subtration.
print(first_num * second_num * third_num)  # Multiplication.
print(first_num / second_num / third_num)  # Division.
print(first_num + second_num - third_num)  #Add & Sub